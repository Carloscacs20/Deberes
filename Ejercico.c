#include <stdio.h>

int main (int argc, char *argv[]) {

    int a[3][4]={{2,3,4,5},{5,3,6,7},{7,8,9,10}};
    int b[4][2]={{3,4},{10,3},{6,5},{4,6}};

    int r[3][2]={{0,0},{0,0},{0,0}};
    for (int i = 0; i < 3; i++) //filas de la resultante
    {
        for (int j = 0; j < 2; j++) // columnas de la resultante
        {
            for (int z = 0; z < 4; z++) // numero comun de la resultante
            {
                r[i][j]+=a[i][z]*b[z][j];
            }
            printf("%d\t",r[i][j]);
        }
        printf("\n");
    }
    
    r[0][0]=a[0][0]*b[0][0]+a[0][1]*b[1][0]+a[0][2]*b[2][0]+a[0][3]*b[3][0];
    r[0][1]=a[0][0]*b[0][1]+a[0][1]*b[1][1]+a[0][2]*b[2][1]+a[0][3]*b[3][1];

    r[1][0]=a[1][0]*b[0][0]+a[1][1]*b[1][0]+a[1][2]*b[2][0]+a[1][3]*b[3][0];
    r[1][1]=a[1][0]*b[0][1]+a[1][1]*b[1][1]+a[1][2]*b[2][1]+a[1][3]*b[3][1];

    r[2][0]=a[2][0]*b[0][0]+a[2][1]*b[1][0]+a[2][2]*b[2][0]+a[2][3]*b[3][0];
    r[2][1]=a[2][0]*b[0][1]+a[2][1]*b[1][1]+a[2][2]*b[2][1]+a[2][3]*b[3][1];

    printf("%d \t %d",r[0][0],r[0][1]);
    printf("%d \t %d",r[1][0],r[1][1]);
    printf("%d \t %d",r[2][0],r[2][1]);
    return 0;
}